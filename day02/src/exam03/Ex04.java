public class Ex04 {
    public static void main(String[] args){
        double num3 = 10.0 / 3;
        double num4 = 10 / 3;
        System.out.println(num3);
        System.out.println(num4);

        int num1 = 10 / 3;
        int num2 = 10 % 3;
        System.out.println(num1);
        System.out.println(num2);
        System.out.printf("목 = %d, 나머지 = %d%n", num1, num2);
    }
}
